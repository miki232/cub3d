#include "../include/cub.h"

void    ft_square(t_data *mlx, int colo)
{
    int x;
    int y;

    y = 0;
    while (y < 10)
    {
        x = 0;
        while (x < 10)
        {
            my_mlx_pixel_put(mlx, x, y, colo);
            x++;
        }
        y++;
    }
}